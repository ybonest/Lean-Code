// 有⼀楼梯共10级，刚开始时你在第⼀级，若每次只能跨上⼀级或⼆级，要⾛上第10级，共有多少种⾛法？

/**
 * 到第10级楼梯有两种方式，从10 - 1 = 9， 10 - 2 = 8
 * 即从9或8到第十级，又到第九级 9 - 1 = 8， 9 - 2 = 7
 * 到第八级 8 - 1 = 7， 8 - 2 = 6 依次往下
 */

function stairs(n) {
  if (n === 0) {
    return 1;
  }
  if (n < 0) {
    return 0;
  }
  return stairs(n - 1) + stairs(n - 2);
}

console.log(stairs(10))